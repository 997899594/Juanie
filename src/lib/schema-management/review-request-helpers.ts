export interface GeneratedSchemaRepairArtifacts {
  files: Record<string, string>;
  generatedFiles: string[];
  migrationTag: string | null;
}

interface SchemaRepairArtifactInput {
  provider: 'github' | 'gitlab' | 'gitlab-self-hosted';
  tool: 'drizzle' | 'sql' | 'prisma' | 'knex' | 'typeorm' | 'custom';
  databaseType: 'postgresql' | 'mysql' | 'redis' | 'mongodb';
  migrationPath: string | null;
  existingMigrationNames: string[];
  journalContent?: string | null;
  planId: string;
  title: string;
  summary: string;
  planKind:
    | 'no_action'
    | 'run_release_migrations'
    | 'mark_aligned'
    | 'repair_pr_required'
    | 'adopt_current_db'
    | 'manual_investigation';
  stateStatus: string;
  databaseName: string;
  expectedVersion: string | null;
  actualVersion: string | null;
}

function parseNumericPrefix(value: string): number | null {
  const match = value.match(/^(\d+)_/u);
  if (!match?.[1]) {
    return null;
  }

  return Number.parseInt(match[1], 10);
}

export function buildNextMigrationTag(existingNames: string[], stem: string): string {
  const parsed = existingNames
    .map((name) => name.replace(/\.(sql|js)$/u, ''))
    .map((name) => ({
      name,
      prefix: parseNumericPrefix(name),
    }))
    .filter((item) => item.prefix !== null);

  const maxPrefix = parsed.reduce((current, item) => Math.max(current, item.prefix ?? 0), -1);
  const width = parsed.reduce((current, item) => {
    const match = item.name.match(/^(\d+)_/u);
    return Math.max(current, match?.[1]?.length ?? 4);
  }, 4);

  return `${String(maxPrefix + 1).padStart(width, '0')}_${stem}`;
}

export function appendDrizzleJournalEntry(
  journalContent: string | null | undefined,
  tag: string,
  when: number
): string {
  const journal = journalContent
    ? (JSON.parse(journalContent) as {
        version?: string;
        dialect?: string;
        entries?: Array<{
          idx?: unknown;
          version?: unknown;
          when?: unknown;
          tag?: unknown;
          breakpoints?: unknown;
        }>;
      })
    : {};

  const version = typeof journal.version === 'string' ? journal.version : '7';
  const dialect = typeof journal.dialect === 'string' ? journal.dialect : 'postgresql';
  const existingEntries = Array.isArray(journal.entries) ? journal.entries : [];
  const nextIdx =
    existingEntries.reduce((current, entry) => {
      const idx = typeof entry?.idx === 'number' ? entry.idx : -1;
      return Math.max(current, idx);
    }, -1) + 1;

  const next = {
    version,
    dialect,
    entries: [
      ...existingEntries,
      {
        idx: nextIdx,
        version,
        when,
        tag,
        breakpoints: true,
      },
    ],
  };

  return `${JSON.stringify(next, null, 2)}\n`;
}

function buildFailureScaffoldMessage(input: SchemaRepairArtifactInput): string {
  return [
    `Generated by Juanie schema management.`,
    `Plan: ${input.planId}`,
    `Kind: ${input.planKind}`,
    `Database: ${input.databaseName}`,
    `Expected: ${input.expectedVersion ?? 'unknown'}`,
    `Actual: ${input.actualVersion ?? 'unknown'}`,
    `Title: ${input.title}`,
    `Summary: ${input.summary}`,
    `Replace this scaffold with the real repair migration before merge.`,
  ].join(' | ');
}

function buildPlanMetadataContent(input: SchemaRepairArtifactInput): string {
  return `${JSON.stringify(
    {
      planId: input.planId,
      title: input.title,
      summary: input.summary,
      planKind: input.planKind,
      stateStatus: input.stateStatus,
      databaseName: input.databaseName,
      tool: input.tool,
      databaseType: input.databaseType,
      migrationPath: input.migrationPath,
      expectedVersion: input.expectedVersion,
      actualVersion: input.actualVersion,
    },
    null,
    2
  )}\n`;
}

function buildAtlasConfigContent(input: SchemaRepairArtifactInput): string {
  const envBlock =
    input.tool === 'drizzle'
      ? [
          `env "repair" {`,
          `  src = data.external_schema.drizzle.url`,
          `  dev = env("ATLAS_DEV_URL")`,
          `  migration {`,
          `    dir = "file://${input.migrationPath}"`,
          `  }`,
          `}`,
          ``,
          `data "external_schema" "drizzle" {`,
          `  program = ["npx", "drizzle-kit", "export"]`,
          `}`,
          ``,
        ]
      : [
          `env "repair" {`,
          `  src = env("ATLAS_SRC_URL")`,
          `  dev = env("ATLAS_DEV_URL")`,
          `  migration {`,
          `    dir = "file://${input.migrationPath}"`,
          `  }`,
          `}`,
          ``,
        ];

  return [
    `# Generated by Juanie schema management`,
    `# Plan: ${input.planId}`,
    `# Kind: ${input.planKind}`,
    `# Tool: ${input.tool}`,
    `# Database: ${input.databaseName}`,
    ``,
    ...envBlock,
  ].join('\n');
}

function buildAtlasScriptContent(
  input: SchemaRepairArtifactInput,
  atlasConfigPath: string
): string {
  const migrationName =
    input.planKind === 'adopt_current_db'
      ? `baseline_${input.planId.slice(0, 8)}`
      : `repair_${input.planId.slice(0, 8)}`;

  return [
    `#!/usr/bin/env bash`,
    `set -euo pipefail`,
    ``,
    `# Generated by Juanie schema management.`,
    `# Prefer Atlas for real migration generation instead of editing scaffold SQL by hand.`,
    `# Required env: ATLAS_DEV_URL and${input.tool === 'sql' ? ' ATLAS_SRC_URL' : ''} optional ATLAS_IMAGE`,
    ``,
    `ATLAS_IMAGE="\${ATLAS_IMAGE:-arigaio/atlas:latest}"`,
    `docker run --rm \\`,
    `  -v "$PWD:/workspace" \\`,
    `  -w /workspace \\`,
    `  -e ATLAS_DEV_URL="${'$'}{ATLAS_DEV_URL:-}" \\`,
    input.tool === 'sql' ? `  -e ATLAS_SRC_URL="${'$'}{ATLAS_SRC_URL:-}" \\` : '',
    `  "$ATLAS_IMAGE" migrate diff "${migrationName}" \\`,
    `  --env repair \\`,
    `  --config "file://${atlasConfigPath}"`,
    ``,
  ]
    .filter(Boolean)
    .join('\n');
}

function buildGitHubAtlasWorkflow(input: { planId: string; atlasScriptPath: string }): string {
  return [
    `name: Schema Repair ${input.planId.slice(0, 8)}`,
    `on:`,
    `  pull_request:`,
    `    branches: [main, master]`,
    `jobs:`,
    `  atlas-repair:`,
    `    runs-on: ubuntu-latest`,
    `    steps:`,
    `      - uses: actions/checkout@v5`,
    `      - uses: ariga/setup-atlas@v0`,
    `      - run: chmod +x ${input.atlasScriptPath}`,
    `      - run: ${input.atlasScriptPath}`,
    ``,
  ].join('\n');
}

function buildGitLabAtlasSnippet(input: { planId: string; atlasScriptPath: string }): string {
  const jobName = `schema_repair_${input.planId.slice(0, 8)}`;

  return [
    `${jobName}:`,
    `  stage: test`,
    `  image: arigaio/atlas:latest`,
    `  script:`,
    `    - chmod +x ${input.atlasScriptPath}`,
    `    - ${input.atlasScriptPath}`,
    `  rules:`,
    `    - if: $CI_PIPELINE_SOURCE == "merge_request_event"`,
    ``,
  ].join('\n');
}

function buildScaffoldHeading(input: SchemaRepairArtifactInput): string[] {
  const modeSummary =
    input.planKind === 'adopt_current_db'
      ? 'baseline scaffold'
      : input.planKind === 'repair_pr_required'
        ? 'repair scaffold'
        : 'investigation scaffold';

  return [
    `-- Generated by Juanie schema management`,
    `-- Plan ID: ${input.planId}`,
    `-- Plan kind: ${input.planKind}`,
    `-- State status: ${input.stateStatus}`,
    `-- Database: ${input.databaseName}`,
    `-- Mode: ${modeSummary}`,
    `-- Expected version: ${input.expectedVersion ?? 'unknown'}`,
    `-- Actual version: ${input.actualVersion ?? 'unknown'}`,
    `-- Title: ${input.title}`,
    `-- Summary: ${input.summary}`,
    `-- Replace this scaffold before merge.`,
    ``,
  ];
}

export function buildSchemaRepairArtifacts(
  input: SchemaRepairArtifactInput
): GeneratedSchemaRepairArtifacts {
  const metadataPath = `.juanie/schema-repair/${input.planId}.json`;
  const atlasConfigPath = `.juanie/schema-repair/${input.planId}.atlas.hcl`;
  const atlasScriptPath = `.juanie/schema-repair/${input.planId}.atlas.sh`;
  const files: Record<string, string> = {
    [metadataPath]: buildPlanMetadataContent(input),
    [atlasConfigPath]: buildAtlasConfigContent(input),
    [atlasScriptPath]: buildAtlasScriptContent(input, atlasConfigPath),
  };
  const generatedFiles = [metadataPath, atlasConfigPath, atlasScriptPath];

  if (input.provider === 'github') {
    const workflowPath = `.github/workflows/schema-repair-${input.planId.slice(0, 8)}.yml`;
    files[workflowPath] = buildGitHubAtlasWorkflow({
      planId: input.planId,
      atlasScriptPath,
    });
    generatedFiles.push(workflowPath);
  } else {
    const gitlabSnippetPath = `.juanie/schema-repair/${input.planId}.gitlab-ci.yml`;
    files[gitlabSnippetPath] = buildGitLabAtlasSnippet({
      planId: input.planId,
      atlasScriptPath,
    });
    generatedFiles.push(gitlabSnippetPath);
  }

  const needsMigrationScaffold =
    input.migrationPath &&
    ['drizzle', 'sql'].includes(input.tool) &&
    ['repair_pr_required', 'adopt_current_db'].includes(input.planKind);

  if (!needsMigrationScaffold || !input.migrationPath) {
    return {
      files,
      generatedFiles,
      migrationTag: null,
    };
  }

  const stemPrefix =
    input.planKind === 'adopt_current_db' ? 'juanie_schema_baseline' : 'juanie_schema_repair';
  const stem = `${stemPrefix}_${input.planId.slice(0, 8)}`;
  const migrationTag = buildNextMigrationTag(input.existingMigrationNames, stem);
  const filePath = `${input.migrationPath}/${migrationTag}.sql`;
  const message = buildFailureScaffoldMessage(input);
  const heading = buildScaffoldHeading(input);

  const failureSql =
    input.databaseType === 'postgresql'
      ? [
          ...heading,
          `DO $$`,
          `BEGIN`,
          `  RAISE EXCEPTION '${message.replace(/'/g, "''")}';`,
          `END $$;`,
          ``,
        ].join('\n')
      : input.databaseType === 'mysql'
        ? [
            ...heading,
            `SIGNAL SQLSTATE '45000'`,
            `  SET MESSAGE_TEXT = '${message.replace(/'/g, "''")}';`,
            ``,
          ].join('\n')
        : `${heading.join('\n')}JUANIE_REPAIR_SCAFFOLD_REPLACE_ME;\n`;

  if (input.tool === 'sql') {
    return {
      files: {
        ...files,
        [filePath]: failureSql,
      },
      generatedFiles: [...generatedFiles, filePath],
      migrationTag,
    };
  }

  const journalPath = `${input.migrationPath}/meta/_journal.json`;

  return {
    files: {
      ...files,
      [filePath]: failureSql,
      [journalPath]: appendDrizzleJournalEntry(input.journalContent, migrationTag, Date.now()),
    },
    generatedFiles: [...generatedFiles, filePath, journalPath],
    migrationTag,
  };
}
